function applyLanguage(lang) {
    if (!translations[lang]) lang = 'tr';
    localStorage.setItem('fontify_lang', lang);
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });
    
    // Dropdown Checkmarks
    const trBtn = document.getElementById('langTrBtn');
    const enBtn = document.getElementById('langEnBtn');
    if(trBtn && enBtn) {
        trBtn.innerHTML = lang === 'tr' ? '<i class="bi bi-check2"></i> TÃ¼rkÃ§e' : 'TÃ¼rkÃ§e';
        enBtn.innerHTML = lang === 'en' ? '<i class="bi bi-check2"></i> English' : 'English';
    }
}

// Auto Init
document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('fontify_lang') || 'tr';
    applyLanguage(saved);
    
    document.getElementById('langTrBtn')?.addEventListener('click', (e) => { e.preventDefault(); applyLanguage('tr'); });
    document.getElementById('langEnBtn')?.addEventListener('click', (e) => { e.preventDefault(); applyLanguage('en'); });
});